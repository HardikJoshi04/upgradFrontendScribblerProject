$(document).ready(function(){
    $("#trashDeletepost").click(function(){
        $("#exampleModal").modal();
        $("#btnYes").click(function(){
            document.getElementById("boxFirst").style.display="none";
            $('#exampleModal').modal('hide');
        });
        $('#btnNo').click(function() {
        $('#exampleModal').modal('hide');
});
    });
    });

